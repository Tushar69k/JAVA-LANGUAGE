package PracticeWork;

public class DataTypes_2 {
    public static void main(String[] args) {

        // 1 BYTE = 8 BITES

        byte ex_byte = 2;  //  1 BYTE OR 8 BITES

        int ex_int = 56;  //  4 BYTES OR 32 BITES

        short ex_short = 87; //  2 BYTES OR 16 BITES

        long ex_long = 56666666666666L; //  8 BYTE OR 64 BITES
        // USE L TO USE LONG

        char ex_char = 'T'; // 2 BYTES OR 16 BITES
        // CAN STORE UNICODE CHARACTERS ( SUPERCLASS OR ASCII STANDARD )

        float ex_float = 6.9f; // 4 BYTES OR 32 BITES
        // MUST INCLUDE f AFTER DECIMAL NUMBER FOR FLOAT DATATYPE

        double ex_double = 69.69D; // 8 BYTES OR 64 BITES
        // EVERY DECIML NUMBER IS double BY DEFAULT
        // MUST USE D OR d AFTER NUMBER

        boolean ex_boolean = true; // SIZE DEPENDS OF JVM
        // CAN STORE TRUE OR FALSE

        // LITERALS MEANS THE VALUE AFTER =
        // SYNTAX ---> DATATYPE = LITERAL

        System.out.println(ex_byte);
        System.out.println("WELCOME ANOTHER THING TO YOUR BRAIN BUDDY ");

    }
}
